public class TypeCast {
        public static void main(String [] args){
            char a='j';
            int i=(int)a;
            System.out.println(i);
            char b='e';
            i=(int)b;
            System.out.println(i);
            char c='r';
            i=(int)c;
            System.out.println(i);
            char d='i';
            i=(int)d;
            System.out.println(i);
            char e='n';
            i=(int)e;
            System.out.println(i);
        }
    }

